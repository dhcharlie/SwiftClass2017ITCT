import UIKit

//1. VAR

var variableString = "Apple"
//수정이 가능한 값 (Mutable)

variableString += " and Mango"
//Compile 에러 나지 않음



//2. LET

let constantString = "Apple"
//수정이 불가능한 값 (Immutable)

constantString += " and Banana"
//Compile 에러 발생 (Change 'let' to 'var' to make it mutable)



//3. VAR AND LET

let variableString = "Apple"

var variableString = "Fruits"




